from dataclasses import dataclass, field
from typing import List, Dict, Optional


@dataclass(frozen=True)
class KPI:
    key: str
    name: str
    meaning: str
    why_it_matters: str
    formula: str
    example: str
    unit: str
    better: str
    industries: List[str] = field(default_factory=list)
    improvement_tips: List[str] = field(default_factory=list)
    process: str = ""


import re as _re


def slug(name: str) -> str:
    return _re.sub(r"[^a-z0-9]+", "_", name.lower()).strip("_")


def kpi(name, meaning, why, formula, example, unit, better, industries, tips):
    return {
        "key": slug(name), "name": name, "meaning": meaning,
        "why_it_matters": why, "formula": formula, "example": example,
        "unit": unit, "better": better, "industries": industries,
        "improvement_tips": tips,
    }



KPIS_BY_PROCESS: Dict[str, List[KPI]] = {
    process: [
        KPI(
            key=item["key"], name=item["name"], meaning=item["meaning"],
            why_it_matters=item["why_it_matters"], formula=item["formula"],
            example=item["example"], unit=item["unit"], better=item["better"],
            industries=item.get("industries", []),
            improvement_tips=item.get("improvement_tips", []),
            process=process,
        )
        for item in items
    ]
    for process, items in PROCESS_KPIS.items()
}

_ALL_KPIS: List[KPI] = [k for kpis in KPIS_BY_PROCESS.values() for k in kpis]
_KPI_BY_KEY: Dict[str, KPI] = {k.key: k for k in _ALL_KPIS}


def get_industries() -> List[str]:
    return list(INDUSTRIES)


def get_industry_description(industry: str) -> str:
    return INDUSTRY_DESCRIPTIONS.get(industry, "")


def get_processes_for_industry(industry: str) -> List[str]:
    return list(INDUSTRY_PROCESS_MAPPING.get(industry, []))


def get_all_processes() -> List[str]:
    return sorted(KPIS_BY_PROCESS.keys())


def get_kpis_for_process(process: str) -> List[KPI]:
    return list(KPIS_BY_PROCESS.get(process, []))


def get_all_kpis() -> List[KPI]:
    return list(_ALL_KPIS)


def get_kpi_by_key(key: str) -> Optional[KPI]:
    return _KPI_BY_KEY.get(key)


def get_kpis_for_industry(industry: str) -> List[KPI]:
    kpis: List[KPI] = []
    for process in get_processes_for_industry(industry):
        kpis.extend(get_kpis_for_process(process))
    return kpis


def search_by_keyword(keyword: str) -> List[KPI]:
    kw = keyword.lower()
    return [
        k for k in _ALL_KPIS
        if kw in " ".join([k.name, k.meaning, k.why_it_matters, k.process, k.unit]).lower()
    ]


def filter_kpis(better: Optional[str] = None, unit_contains: Optional[str] = None) -> List[KPI]:
    results = _ALL_KPIS
    if better:
        results = [k for k in results if k.better.lower().startswith(better.lower())]
    if unit_contains:
        results = [k for k in results if unit_contains.lower() in k.unit.lower()]
    return results


def get_glossary() -> Dict[str, str]:
    return dict(GLOSSARY)